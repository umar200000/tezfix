export function translate(lang: Lang, key: string): string {
  return dict[lang]?.[key] ?? dict.uz[key] ?? key;
}

export function useT() {
  const lang = useStore((s) => s.language);
  return (key: string) => translate(lang, key);
}

export function detectInitialLang(): Lang {
  if (typeof window === 'undefined') return 'uz';
  const stored = localStorage.getItem('tezfix-lang') as Lang | null;
  if (stored === 'uz' || stored === 'en' || stored === 'ru') return stored;
  // Fall back to Telegram user's language_code
  const tgLang = (window as any).Telegram?.WebApp?.initDataUnsafe?.user?.language_code as
    | string
    | undefined;
  if (tgLang?.startsWith('ru')) return 'ru';
  if (tgLang?.startsWith('en')) return 'en';
  return 'uz';
}

/** Translate a category by its slug. Falls back to the slug itself if unknown. */
export function categoryName(lang: Lang, slug: string): string {
  return dict[lang]?.[`cat.${slug}`] ?? dict.uz[`cat.${slug}`] ?? slug;
}
